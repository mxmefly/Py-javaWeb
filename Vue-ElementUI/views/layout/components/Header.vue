<template>
    <div class="header" :style="{background: bgcolor}">
        <!-- 折叠按钮 -->
        <div class="collapse-btn" @click="collapseChage">
            <i class="el-icon-menu"></i>
        </div>
        <div class="logo">后台管理系统</div>
        <div class="header-right">
            <div class="header-user-con">
                <div style="width: 30px;height: 30px;">
                    <el-color-picker v-model="bgcolor"></el-color-picker>
                </div>
                <!-- 全屏显示 -->
                <div class="btn-fullscreen" @click="handleFullScreen">
                    <el-tooltip effect="dark" :content="fullscreen?`取消全屏`:`全屏`" placement="bottom">
                        <i class="el-icon-rank"></i>
                    </el-tooltip>
                </div>
                <!-- 消息中心 -->
                <div class="btn-bell">
                    <el-tooltip effect="dark" :content="message?`有${message}条未读消息`:`消息中心`" placement="bottom">
                        <router-link to="/tabs">
                            <i class="el-icon-bell"></i>
                        </router-link>
                    </el-tooltip>
                    <span class="btn-bell-badge" v-if="message"></span>
                </div>
                <!-- 用户头像 -->
                <div class="user-avator" style="width: 40px !important; height: 40px !important;"><img
                        src="assets/images/user/MyHead.jpg"></div>
                <!-- 用户名下拉菜单 -->
                <el-dropdown class="user-name" trigger="click" @command="handleCommand">
                    <span class="el-dropdown-link">
                        {{username}} <i class="el-icon-caret-bottom"></i>
                    </span>
                    <el-dropdown-menu slot="dropdown">
                        <a href="javascript:void(0)" target="_blank">
                            <el-dropdown-item>帮助文档</el-dropdown-item>
                        </a>
                        <el-dropdown-item divided command="loginout">退出登录</el-dropdown-item>
                    </el-dropdown-menu>
                </el-dropdown>
            </div>
        </div>
    </div>
</template>
<script>
    module.exports = {
        data: function () {
            return {
                bgcolor: '#35234E',
                collapse: false,
                fullscreen: false,
                name: 'admin',
                message: 2
            }
        },
        computed: {
            username: function () {
				return this.$store.getters.name;
            }
        },
        methods: {
            // 用户名下拉菜单选择事件
            handleCommand: function (command) {
				var _this=this;
                if (command == 'loginout') {
					this.$store.dispatch('LogOut').then(function () {
					    _this.$router.push({
					        path: '/login'
					    });
					});
                    localStorage.removeItem('ms_username')		
                }
            },
            // 侧边栏折叠
            collapseChage: function () {
                this.collapse = !this.collapse;
                this.$emit('collapse-change', this.collapse);
            },
            // 全屏事件
            handleFullScreen: function () {
                let element = document.documentElement;
                if (this.fullscreen) {
                    if (document.exitFullscreen) {
                        document.exitFullscreen();
                    } else if (document.webkitCancelFullScreen) {
                        document.webkitCancelFullScreen();
                    } else if (document.mozCancelFullScreen) {
                        document.mozCancelFullScreen();
                    } else if (document.msExitFullscreen) {
                        document.msExitFullscreen();
                    }
                } else {
                    if (element.requestFullscreen) {
                        element.requestFullscreen();
                    } else if (element.webkitRequestFullScreen) {
                        element.webkitRequestFullScreen();
                    } else if (element.mozRequestFullScreen) {
                        element.mozRequestFullScreen();
                    } else if (element.msRequestFullscreen) {
                        // IE11
                        element.msRequestFullscreen();
                    }
                }
                this.fullscreen = !this.fullscreen;
            }
        },
        mounted: function () {
            if (document.body.clientWidth < 1200) {
                this.collapseChage();
            }
        }
    }
</script>
